	private static List<string> CreateRangedAnimationList<T>(T startName, T endName)
	{
		List<string> list = Enum.GetNames(typeof(T)).ToList();
		int num = list.IndexOf(startName.ToString());
		int num2 = list.IndexOf(endName.ToString());
		return (from name in list.Skip(num).Take(num2 - num + 1)
			select name.ToLower()).ToList();
	}
