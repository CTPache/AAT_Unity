	private static ushort[] GetSelectTextIds(Info[] info_table, ushort[,] id_table, byte scenario, ushort message_no)
	{
		Info info = null;
		int i;
		for (i = 0; i < info_table.Length; i++)
		{
			info = info_table[i];
			if (info.scenario == scenario && info.message_no == message_no)
			{
				break;
			}
		}
		ushort[] array = null;
		if (i < info_table.Length)
		{
			array = new ushort[2 + info.select_cnt];
			for (int j = 0; j < array.Length; j++)
			{
				array[j] = id_table[i, j];
			}
		}
		return array;
	}

